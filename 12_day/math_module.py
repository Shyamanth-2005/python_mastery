import math

print(math.pi)
print(math.sqrt(2))
print(math.pow(2,3)) # 8.0
print(math.floor(9.81)) # 9 floor -> will increase the rounding to the lowest nearest number
print(math.ceil(9.81)) # 10 ceil -> will increase the rounding to the highest nearest number
print(math.log10(100)) # 2



